Student::Student(int id, string name, string pwd)
{
	this->m_Id = id;
	this->m_Name = name;
	this->m_Pwd = pwd;
	//获取机房信息
	fstream ifs;
	ifs.open(COMPUTER_FILE, ios::in);
	if (!ifs.is_open())
	{
		cout << "文件读取失败" << endl;
		return;
	}
	ComputerRoom c;
	while (ifs >> c.room_number&& ifs >> c.maxnum)
	{
		vRoom.push_back(c);
	}
	cout << "当前机房数量为：" << vRoom.size() << endl;
	ifs.close();
}
